# args:
#   REPO_NAME
#   PACKAGE_IMPORT_NAME
#   GITHUB_USERNAME
#   AUTHOR
#   AUTHOR_EMAIL
function open-pr-with-generated-project {

    # debug
    rm -rf outdir || true
    rm -rf config || true
    rm -rf "$REPO_NAME" || true

    # Clone repository
    gh repo clone "$GITHUB_USERNAME/$REPO_NAME"

    # Delete local repository contents but keep .git file
    mv "$REPO_NAME/.git" "./$REPO_NAME.git.back"
    rm -rf "$REPO_NAME"
    mkdir "$REPO_NAME"
    mv "./$REPO_NAME.git.back" "$REPO_NAME/.git"

    # Generate config file
    mkdir config || true
    CONFIG_FILE_PATH="./config/$REPO_NAME.yaml"
    cat <<EOF > "$CONFIG_FILE_PATH"
default_context:
    repo_name: $REPO_NAME
    package_import_name: $PACKAGE_IMPORT_NAME
    author: $AUTHOR
    author_email: $AUTHOR_EMAIL
EOF
    rm -rf "CONFIG_FILE_PATH"

    # Generate project in outdir
    mkdir outdir || true
    cookiecutter ./ --output-dir=./outdir --no-input --config-file="$CONFIG_FILE_PATH"

    # Move .git into project in outdir
    mv "$REPO_NAME/.git" "./outdir/$REPO_NAME"

    # Checkout new feature branch
    UUID=$(uuidgen)
    UNIQUE_BRANCH_NAME=feat/populating-from-template-${UUID:0:6}

    git config --global user.email "eric.adam.garner@gmail.com"
    git config --global user.name "Eric Adam Garner"

    cd "./outdir/$REPO_NAME"
    git checkout -b "$UNIQUE_BRANCH_NAME"

    # Stage files for linting by pre-commit
    git add --all
    lint:ci || true

    # Restage the files modified by pre-commit
    git add --all

    # Commit changes to feature branch
    git commit -m "feat: generate project from template"

    # Push to remote repository on feature branch
    if [[ -n "$GH_TOKEN" ]]; then
        git remote set-url origin "https://$GITHUB_USERNAME:$GH_TOKEN@github.com/$GITHUB_USERNAME/$REPO_NAME"
    fi
    git push origin "$UNIQUE_BRANCH_NAME"

    # Open pull request into main branch
    gh pr create \
        --title "feat: generate project from template" \
        --body "This PR was generated by python-course-cookiecutter-v2" \
        --base main \
        --head "$UNIQUE_BRANCH_NAME" \
        --repo "$GITHUB_USERNAME/$REPO_NAME"
}

function create-sample-repo {
    gh workflow run .github/workflows/create-or-update-repo.yaml \
        -f repo_name=test-repoZ \
        -f package_import_name=package \
        -f is_public=false \
        --ref main
}

TIMEFORMAT="Task completed in %3lR"
time ${@:-help}
